def normalize_language(value: object) -> str:
    if isinstance(value, str):
        norm = value.strip().lower()
        if norm in LANGUAGE_CHOICES:
            return norm
    return "en"


def detect_default_language() -> str:
    # Prefer explicit environment locale settings first (Linux/Unix, sometimes Windows).
    env_candidates = (
        os.environ.get("LC_ALL"),
        os.environ.get("LANGUAGE"),
        os.environ.get("LC_MESSAGES"),
        os.environ.get("LANG"),
    )
    for raw in env_candidates:
        if not isinstance(raw, str) or not raw.strip():
            continue
        token = raw.split(":", 1)[0].strip().lower()
        if token.startswith("de"):
            return "de"
        if token.startswith("en"):
            return "en"

    # Fallback to system locale (works on Windows and Linux).
    try:
        loc, _enc = locale.getlocale()
    except Exception:
        loc = None
    if isinstance(loc, str):
        lo = loc.lower()
        if lo.startswith("de"):
            return "de"
        if lo.startswith("en"):
            return "en"

    return "en"


def tr(language: str, key: str, **kwargs) -> str:
    lang = normalize_language(language)
    table = STRINGS.get(lang, STRINGS["de"])
    text = table.get(key) or STRINGS["de"].get(key) or key
    if kwargs:
        return text.format(**kwargs)
    return text


def month_label(language: str, month_key: str) -> str:
    parts = month_key.split("-", 1)
    if len(parts) != 2:
        return month_key
    year, month = parts
    lang = normalize_language(language)
    return f"{MONTHS.get(lang, MONTHS['de']).get(month, month)} {year}"
